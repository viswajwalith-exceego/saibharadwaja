// Helper function to pair books for two-column display
export const pairBooks = (books) => {
  const pairs = []
  for (let i = 0; i < books.length; i += 2) {
    pairs.push({
      left: books[i] || null,
      right: books[i + 1] || null
    })
  }
  return pairs
}
